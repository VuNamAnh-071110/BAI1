sc = int(input("Nhap mot so duong: "))


def getEvenNumberBelowChoosen(ChoosenNum):
  evenNum = []
  placeholder_num = 0

  while placeholder_num < ChoosenNum:
    if placeholder_num % 2 == 0:
      evenNum.append(placeholder_num)
    placeholder_num += 1

  return evenNum

